import UIKit

extension UIButton
{
    func selectedbutton()
    {
        self.backgroundColor = #colorLiteral(red: 0.1171855518, green: 0.4559102916, blue: 0.2057636362, alpha: 1)
    }
    
    func deselectedButton()
    {
        self.backgroundColor = #colorLiteral(red: 0.2517644722, green: 0.9794894691, blue: 0.442067921, alpha: 0.876540493)
    }
    
}
